package br.com.desciclopedia.designpatterns;

public class ReinventandoARoda {

	public int imprimeValor(String a) {
		if (a.equals("1")) {
			return 1;
		} else if (a.equals("2")) {
			return 2;
		} else if (a.equals("3")) {
			return 3;
		} else if (a.equals("4")) {
			return 4;
		} else if (a.equals("5")) {
			return 5;
		} else if (a.equals("6")) {
			return 6;
		} else if (a.equals("7")) {
			return 7;
		} else if (a.equals("8")) {
			return 8;
		} else if (a.equals("9")) {
			return 9;
		} else if (a.equals("10")) {
			return 10;
		} else if (a.equals("1000")) {
			return 1000;
		}
		
		return 0;
	}
}
